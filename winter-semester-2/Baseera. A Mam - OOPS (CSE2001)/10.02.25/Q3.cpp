//Program For Increasing & Decreasing Using For Loop

#include <iostream>
using namespace std;

int main() 
{
    cout << "DATE: 10.02.25, Time: 9:49 AM, NAME: Lakshya Kant\n";
    int a;
    cout << "Counting In Ascending Order: ";
    for (a=1;a<11;a++) {cout<< a << " ";}
    cout << endl;
    int d;
    cout << "Counting In Descending Order: ";
    for (d=10;d>0;d--) {cout<< d << " ";}
    return 0;
}